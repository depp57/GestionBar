IS
    v_produit varchar(30);
BEGIN
    FOR i IN 1..40000 LOOP
        SELECT intituleProduit INTO v_produit FROM
            ( SELECT intituleProduit FROM Produit
            ORDER BY dbms_random.value )
            WHERE rownum = 1;

        stock_acheter(v_produit, 1, 1);
    end loop;
end;

